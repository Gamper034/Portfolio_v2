  <script>
    $('#submitForm').click( function () {
      $.ajax({
          url: "/ajax_contact",
          method: "POST",
          // dataType: 'JSON',
          data: {
              firstname: $('#firstname').val(),
              name: $('#name').val(),
              email: $('#email').val(),
              message: $('#message').val(),
          },
          success: function(data) {
              if (data == true) {
                  toastr.success('Votre demande a bien été envoyée.');
                  $( "#submitForm, #firstname, #name, #email, #message" ).prop( "disabled", true );
                  $('#firstname').val('');
                  $('#name').val('');
                  $('#email').val('');
                  $('#message').val('');
              } else {
                  toastr.error(data.message);
              }
          }
      });
    });
  </script>