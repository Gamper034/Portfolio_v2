    $('#submitForm').click( function () {
      $.ajax({
          url: "/ajax_contact",
          method: "POST",
          dataType: 'JSON',
          data: {
              firstname: $('#firstname').val(),
              name: $('#name').val(),
              email: $('#email').val(),
              message: $('#message').val(),
          },
          success: function(data) {
              if (data.isDone == 1) {
                  toastr.success(data.message);
              } else {
                  toastr.error(data.message);
              }

          }
      });
      toastr.success('toto');
    });
