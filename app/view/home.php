    $('#submitForm').click(function() {
        $.ajax({
            url: "/ajax_contact",
            method: "POST",
            // dataType: 'JSON',
            data: {
                firstname: $('#firstname').val(),
                name: $('#name').val(),
                email: $('#email').val(),
                message: $('#message').val(),
            },
            success: function(data) {
                if (data == true) {
